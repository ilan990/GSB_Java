    private void btnValiderMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnValiderMouseClicked
if (tVisiteur.getSelectedRow()<0)
         {
             JOptionPane.showMessageDialog(this, "Veuillez choisir une ligne à modifier");
         }
         else
         {
             if(txtRole.getText().isEmpty())
             {
                  JOptionPane.showMessageDialog(this, "Veuillez renseigner tous les champs");
             }
             else
             {
                if(txtRole.getText().matches( "^[^0-9]+$"))
                 {
                    int idVisiteur=Integer.parseInt(tVisiteur.getValueAt(tVisiteur.getSelectedRow(),0).toString());
                    ConnexionBdd cnx= new ConnexionBdd();
                    fm= new FonctionsMetier();
                    SimpleDateFormat sdf = new SimpleDateFormat("yyyy-MM-dd");
                    String date = sdf.format(jdEmbauche.getDate());
                    fm.InsererTravailVisiteur(idVisiteur,date,cbRegion.getSelectedItem().toString(),txtRole.getText());
                    ActualisationTableau();
                 }
                else
                 {
                     JOptionPane.showMessageDialog(this, "Veuillez entrer que des lettres ");
                 } 
             }
            
                
         }        
    }//GEN-LAST:event_btnValiderMouseClicked

