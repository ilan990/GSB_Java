    private void ActualisationTableau(){
        ConnexionBdd cnx = new ConnexionBdd();
        ModelUser mdlUser = new ModelUser();
        mdlUser.LoadDats10Colonnes(fm.getAllVisiteurs());
        tVisiteur.setModel(mdlUser);
        cnx = new ConnexionBdd();
        ModelTravailler mdlTravail=new ModelTravailler();
        mdlTravail.LoadDats5Colonnes(fm.getAllTravail());
        tblTravailler.setModel(mdlTravail);
        
    }
